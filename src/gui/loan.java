    public void loadLoans() {
        try {
            ResultSet rs = MySQL.search("SELECT * FROM loan_details INNER JOIN bank ON loan_details.bank_id = bank.bank_id INNER JOIN status ON loan_details.status_id = `status`.id ORDER BY (loan_id)ASC");
            DefaultTableModel dtm = (DefaultTableModel) jTable1.getModel();
            dtm.setRowCount(0);
            Vector v;
            while (rs.next()) {
                v = new Vector();
                v.add(rs.getString("loan_id"));
                v.add(rs.getString("bank_name"));
                v.add(rs.getString("loan_name"));
                v.add(rs.getString("Amount_borrowed"));
                v.add(rs.getString("monthly_installment"));
                v.add(rs.getString("months_to_pay"));
                v.add(rs.getString("Remaining_Months"));
                v.add(rs.getString("name"));
                dtm.addRow(v);
            }

        } catch (Exception e) {
            e.printStackTrace();
        }
    }

