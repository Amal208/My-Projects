    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        int clickCount = evt.getClickCount();
        if (clickCount == 1) {
            int row = jTable1.getSelectedRow();

            String loan_id = jTable1.getValueAt(row, 0).toString();
            String bank = jTable1.getValueAt(row, 1).toString();
            String loan_name = jTable1.getValueAt(row, 2).toString();
            String Amount_borrowed = jTable1.getValueAt(row, 3).toString();
            String monthly_installment = jTable1.getValueAt(row, 4).toString();
            String months_to_pay = jTable1.getValueAt(row, 5).toString();
            String Remaining_Months = jTable1.getValueAt(row, 6).toString();
            String status = jTable1.getValueAt(row, 7).toString();

            jTextField1.setText(loan_id);
            jComboBox2.setSelectedItem(bank);
            jTextField2.setText(loan_name);
            jTextField5.setText(Amount_borrowed);
            jTextField3.setText(monthly_installment);
            jTextField6.setText(months_to_pay);
            jTextField4.setText(Remaining_Months);
            jComboBox1.setSelectedItem(status);
        }
    }//GEN-LAST:event_jTable1MouseClicked

    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        int row = jTable1.getSelectedRow();
        if (JOptionPane.showConfirmDialog(this, "Do you want to remove the Loan Details?", "WARNING", JOptionPane.YES_NO_OPTION) == JOptionPane.YES_OPTION) {
            String id = jTable1.getValueAt(row, 0).toString();
            MySQL.iud("DELETE FROM `svaba`.`loan_details` WHERE `loan_id`=" + id + "");
            loadLoans();
        }
    }//GEN-LAST:event_jButton3ActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        try {
            int row = jTable1.getSelectedRow();

            String loan_id = jTextField1.getText();
            String bank = jComboBox2.getSelectedItem().toString();
            String loan_name = jTextField2.getText();
            String Amount_borrowed = jTextField5.getText();
            String monthly_installment = jTextField3.getText();
            String months_to_pay = jTextField6.getText();
            String Remaining_Months = jTextField4.getText();
            String status = jComboBox1.getSelectedItem().toString();

            ResultSet rs1 = MySQL.search("SELECT `bank_id` FROM `bank` WHERE `bank_name`='" + bank + "'");
            rs1.next();
            ResultSet rs2 = MySQL.search("SELECT `id` FROM `status` WHERE `name`='" + status + "'");
            rs2.next();

            MySQL.iud("UPDATE `loan_details` SET `loan_id`='" + loan_id + "',`bank_id`='" + rs1.getString("bank_id") + "',`loan_name`='" + loan_name + "',`Amount_borrowed`='" + Amount_borrowed + "',`monthly_installment`='" + monthly_installment + "',`months_to_pay`='" + months_to_pay + "',`Remaining_Months`='" + Remaining_Months + "',`status_id`='" + rs2.getString("id") + "' WHERE `loan_id`='" + jTable1.getValueAt(row, 0).toString() + "'");

            JOptionPane.showMessageDialog(this, "Loan Details updated", "Success", JOptionPane.INFORMATION_MESSAGE);
            loadLoans();
        } catch (Exception e) {
            e.printStackTrace();
        }
    }//GEN-LAST:event_jButton2ActionPerformed

    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String loan_id = jTextField1.getText();
        String bank = jComboBox2.getSelectedItem().toString();
        String loan_name = jTextField2.getText();
        String Amount_borrowed = jTextField5.getText();
        String monthly_installment = jTextField3.getText();
        String months_to_pay = jTextField6.getText();
        String Remaining_Months = jTextField4.getText();
        String status = jComboBox1.getSelectedItem().toString();

        if (loan_id.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please enter ID", "Warning", JOptionPane.WARNING_MESSAGE);
        } else if (bank.equals("Select")) {
            JOptionPane.showMessageDialog(this, "Please Select Bank", "Warning", JOptionPane.WARNING_MESSAGE);
        } else if (loan_name.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please enter Loan Name", "Warning", JOptionPane.WARNING_MESSAGE);
        } else if (Amount_borrowed.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please enter Borrowed Amount", "Warning", JOptionPane.WARNING_MESSAGE);
        } else if (monthly_installment.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please enter Monthly Installment", "Warning", JOptionPane.WARNING_MESSAGE);
        } else if (months_to_pay.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please enter Months to Pay", "Warning", JOptionPane.WARNING_MESSAGE);
        } else if (Remaining_Months.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please enter Remaining Months", "Warning", JOptionPane.WARNING_MESSAGE);
        } else if (status.equals("Select")) {
            JOptionPane.showMessageDialog(this, "Please Select Status", "Warning", JOptionPane.WARNING_MESSAGE);
        } else {
            try {
                ResultSet rs1 = MySQL.search("SELECT `bank_id` FROM `bank` WHERE `bank_name`='" + bank + "'");
                rs1.next();
                ResultSet rs2 = MySQL.search("SELECT `id` FROM `status` WHERE `name`='" + status + "'");
                rs2.next();

                int row = jTable1.getSelectedRow();

                MySQL.iud("INSERT INTO `loan_details`(`loan_id`,`bank_id`,`loan_name`,`Amount_borrowed`,`monthly_installment`,`months_to_pay`,`Remaining_Months`,`status_id`) VALUES ('" + loan_id + "','" + rs1.getString("bank_id") + "','" + loan_name + "','"+Amount_borrowed+"','"+monthly_installment+"','"+months_to_pay+"','"+Remaining_Months+"','"+rs2.getString("id")+"')  ");
                JOptionPane.showMessageDialog(this, "New Activity Added", "SUCCESS", JOptionPane.INFORMATION_MESSAGE);
                loadLoans();

            } catch (Exception e) {
                e.printStackTrace();
            }
        }
    }//GEN-LAST:event_jButton1ActionPerformed

