    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        int clickCount = evt.getClickCount();
        if (clickCount == 1) {
            int row = jTable1.getSelectedRow();

            String id = jTable1.getValueAt(row, 0).toString();
            String name = jTable1.getValueAt(row, 1).toString();
            String category = jTable1.getValueAt(row, 2).toString();
            String city = jTable1.getValueAt(row, 3).toString();

            jTextField1.setText(id);
            jTextField2.setText(name);
            jComboBox2.setSelectedItem(category);
            jComboBox1.setSelectedItem(city);
        }

    }//GEN-LAST:event_jTable1MouseClicked

    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        try {
            int row = jTable1.getSelectedRow();

            String id = jTextField1.getText();
            String name = jTextField2.getText();
            String category = jComboBox2.getSelectedItem().toString();
            String city = jComboBox1.getSelectedItem().toString();

            ResultSet rs1 = MySQL.search("SELECT `category_id` FROM `category` WHERE `category_name`='" + category + "'");
            rs1.next();
            
            ResultSet rs2 = MySQL.search("SELECT `city_id` FROM `city` WHERE `city_name`='" + city + "'");
            rs2.next();

            MySQL.iud("UPDATE `product` SET `product_id`='" + id + "',`product_name`='" + name + "',`category_id`='" + rs1.getString("category_id") + "',`city_id`='" + rs2.getString("city_id") + "' WHERE `product_id`='"+jTable1.getValueAt(row, 0).toString()+"'");
            
            JOptionPane.showMessageDialog(this, "Product Updated", "Success", JOptionPane.INFORMATION_MESSAGE);
            loadProducts();
        } catch (Exception e) {
            e.printStackTrace();
        }
    }//GEN-LAST:event_jButton1ActionPerformed

