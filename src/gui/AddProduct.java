    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        String product_id = jTextField1.getText();
        String product_name = jTextField2.getText();
        String city = jComboBox1.getSelectedItem().toString();
        String category = jComboBox2.getSelectedItem().toString();

        if (product_id.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please enter Product ID", "Warning", JOptionPane.WARNING_MESSAGE);
        } else if (product_name.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please enter Product Name", "Warning", JOptionPane.WARNING_MESSAGE);
        } else if (city.equals("Select")) {
            JOptionPane.showMessageDialog(this, "Please select City", "Warning", JOptionPane.WARNING_MESSAGE);
        } else if (category.equals("Select")) {
            JOptionPane.showMessageDialog(this, "Please select Category", "Warning", JOptionPane.WARNING_MESSAGE);
        } else {
            try {
                ResultSet rs1 = MySQL.search("SELECT `city_id` FROM `city` WHERE `city_name`='" + city + "'");
                rs1.next();

                ResultSet rs2 = MySQL.search("SELECT `category_id` FROM `category` WHERE `category_name`='" + category + "'");
                rs2.next();

                String city_id = rs1.getString("city_id");
                String category_id = rs2.getString("category_id");

                MySQL.iud("INSERT INTO `product` (`product_id`,`product_name`,`category_id`,`city_id`) VALUES ('" + product_id + "','" + product_name + "','" + category_id + "','" + city_id + "')");
                loadProducts();

                JOptionPane.showMessageDialog(this, "Product Added", "Success", JOptionPane.INFORMATION_MESSAGE);

            } catch (Exception e) {
                e.printStackTrace();
            }
        }
    }//GEN-LAST:event_jButton2ActionPerformed

