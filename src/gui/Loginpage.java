    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String username = jTextField1.getText();
        char[] passwordarray = jPasswordField1.getPassword();
        String password = String.valueOf(passwordarray);
        try {
            if (jTextField1.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "please enter your username", "Warning", JOptionPane.WARNING_MESSAGE);
        } else if (password.isEmpty()) {
            JOptionPane.showMessageDialog(this, "please enter your password", "Warning", JOptionPane.WARNING_MESSAGE);
        } else {
            ResultSet rs = MySQL.search("SELECT * FROM `user` WHERE `username`='" + username + "' AND `password`='" + password + "'");

                        if (rs.next()) {
                            String x = rs.getString("category_id");
                            if("1".equals(x)){
                                JOptionPane.showMessageDialog(this, "Login success", "Success", JOptionPane.INFORMATION_MESSAGE);
                                Userpage up = new Userpage();
                                this.dispose();
                                up.setVisible(true);
                            }
                            
                        }else{
                            JOptionPane.showMessageDialog(this, "This account does not exist. Please create a new account.", "Warning", JOptionPane.WARNING_MESSAGE);
                        }
        }
        } catch (Exception e) {
            e.printStackTrace();
        }
    }//GEN-LAST:event_jButton1ActionPerformed

